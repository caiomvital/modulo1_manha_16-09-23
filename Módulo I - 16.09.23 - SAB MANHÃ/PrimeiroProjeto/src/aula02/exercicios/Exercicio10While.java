package aula02.exercicios;

public class Exercicio10While {

	public static void main(String[] args) {
//Desenvolva um programa que use um loop while 
//para gerar uma sequência de números pares 
//a partir de 2 e imprima cada número na tela 
//até que o número gerado seja maior do que 20.
		
		int contador = 2;
		
		while(contador <= 20) {
			System.out.println(contador);
			contador += 2;
		}
	
	
	
	}

}
