package aula02.exercicios;

public class Exercicio06While {

	public static void main(String[] args) {
//Faça um programa que inicia uma contagem 
//regressiva a partir de 10 até 1, 
//imprimindo cada número na tela.
		
int contador = 10;
		while(contador >=1) {
			System.out.println("Volta nº " + contador);
			contador--;
		}

	}

}
